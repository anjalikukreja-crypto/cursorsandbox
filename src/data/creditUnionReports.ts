const categories = ['Lending', 'Deposits', 'Compliance', 'Operations', 'Marketing', 'Risk', 'Membership', 'Technology']
const statuses = ['Active', 'Pending', 'Scheduled', 'Draft', 'Archived']
const emails = ['admin@eltropy.com', 'reports@creditunion.com', 'compliance@creditunion.com', 'analytics@creditunion.com', 'ops@creditunion.com']

function randomPick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function randomBool(): boolean {
  return Math.random() > 0.5
}

export function generateCreditUnionReports(): CreditUnionReport[] {
  return reportNames.slice(0, 120).map((name, i) => ({
    id: `report-${i + 1}`,
    reportName: name,
    autoGenerate: randomBool(),
    scheduled: randomBool(),
    category: randomPick(categories),
    status: randomPick(statuses),
    contactEmail: randomPick(emails),
  }))
}
